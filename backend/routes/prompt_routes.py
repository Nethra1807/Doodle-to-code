def generate_llm_ui(prompt_text, predicted_label):
    api_key = os.environ.get("GEMINI_API_KEY")
    if not api_key:
        print("⚠️ GEMINI_API_KEY not found. Falling back to mock generator.")
        return _mock_generator(predicted_label)
        
    try:
        import google.generativeai as genai
        
        genai.configure(api_key=api_key)
        model = genai.GenerativeModel("gemini-1.5-flash-latest")
        
        system_prompt = f"""You are an expert Frontend Developer.
Generate a clean, responsive UI using HTML and Tailwind CSS.
Page type: {predicted_label}

Requirements:
- Modern UI, proper spacing, mobile responsive
- Use appropriate semantic HTML components
- Use Tailwind CSS classes for styling
- Do NOT include ```html markdown tags
- Return ONLY the raw HTML code. Do NOT wrap it in <html> or <body>.
- Include a parent container like <div class="p-8 ...">"""
        
        response = model.generate_content(
            f"{system_prompt}\n\nUser Request: {prompt_text}"
        )
        
        content = ""
        if hasattr(response, "text") and response.text:
            content = response.text.strip()
        elif response.candidates:
            content = response.candidates[0].content.parts[0].text.strip()
        else:
            raise Exception("No response generated by Gemini")

        # Clean up markdown if AI still adds it
        if content.startswith("```html"):
            content = content[7:]
        elif content.startswith("```"):
            content = content[3:]
        if content.endswith("```"):
            content = content[:-3]
            
        return content.strip()
    except Exception as e:
        traceback.print_exc()
        print(f"❌ Gemini API call failed: {e}. Falling back to mock generator.")
        return _mock_generator(predicted_label)


